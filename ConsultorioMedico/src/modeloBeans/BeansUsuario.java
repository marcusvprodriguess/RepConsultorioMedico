
package modeloBeans;

/**
 * @author Jessica
 */

public class BeansUsuario {
    
    private Integer cd_usuario;
    private String nm_usuario;
    private String ds_senha;  
    private String tipo_usuario;    
    private String pesquisaUsuario;

    /**
     * @return the cd_usuario
     */
    public int getCd_usuario() {
        return cd_usuario;
    }

    /**
     * @param cd_usuario the cd_usuario to set
     */
    public void setCd_usuario(int cd_usuario) {
        this.cd_usuario = cd_usuario;
    }

    /**
     * @return the nm_usuario
     */
    public String getNm_usuario() {
        return nm_usuario;
    }

    /**
     * @param nm_usuario the nm_usuario to set
     */
    public void setNm_usuario(String nm_usuario) {
        this.nm_usuario = nm_usuario;
    }

    /**
     * @return the ds_senha
     */
    public String getDs_senha() {
        return ds_senha;
    }

    /**
     * @param ds_senha the ds_senha to set
     */
    public void setDs_senha(String ds_senha) {
        this.ds_senha = ds_senha;
    }

    /**
     * @return the tipo_usuario
     */
    public String getTipo_usuario() {
        return tipo_usuario;
    }

    /**
     * @param tipo_usuario the tipo_usuario to set
     */
    public void setTipo_usuario(String tipo_usuario) {
        this.tipo_usuario = tipo_usuario;
    }

    /**
     * @return the pesquisaUsuario
     */
    public String getPesquisaUsuario() {
        return pesquisaUsuario;
    }

    /**
     * @param pesquisaUsuario the pesquisaUsuario to set
     */
    public void setPesquisaUsuario(String pesquisaUsuario) {
        this.pesquisaUsuario = pesquisaUsuario;
    }
   
    
}
